<?php
function convertMonth($month) {
  if("Jan" == $month) {
    return "01";
  }
  if("Feb" == $month) {
    return "02";
  }
  if("Mar" == $month) {
    return "03";
  }
  if("Apr" == $month) {
    return "04";
  }
  if("May" == $month) {
    return "05";
  }
  if("Jun" == $month) {
    return "06";
  }
  if("Jul" == $month) {
    return "07";
  }
  if("Aug" == $month) {
    return "08";
  }
  if("Sep" == $month) {
    return "09";
  }
  if("Oct" == $month) {
    return "10";
  }
  if("Nov" == $month) {
    return "11";
  }
  if("Dec" == $month) {
    return "12";
  }
}
?>
